#Functions can accept more than one parameter

#Example:
def gifts(first, second):
    print("Your first choice for a birthday gift would be " +  first)
    print("Your second choice for a birthday gift would be " + second)

gifts("bike", "basketball")
gifts("speaker", "tickets")
#Your first choice for a birthday gift would be bike
#Your second choice for a birthday gift would be basketball
#Your first choice for a birthday gift would be speaker
#Your second choice for a birthday gift would be tickets